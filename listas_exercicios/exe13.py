"""
Exercício 13: Números pares até 20 (while) Mostre todos os números pares de 2 até
20 usando while.

"""

def pares_ate_20():
    """
    Imprime todos os números pares de 2 até 20 usando while.
    """
    numero = 2
    while numero <= 20:
        print(numero)
        numero += 2

# Programa principal
pares_ate_20()


