"""Operações com Strings: Para a frase "Python é incrível!", faça:

a) Conte o número de caracteres.
b) Transforme em maiúsculas.
c) Substitua "incrível" por "poderoso".
"""

frase = "Python é incrível!"
print(len(frase)) # Número de caracteres
print(frase.upper()) # Transformar em maíscula
print(frase.replace("incrível", "poderoso")) # Substituir 'incrível' por 'poderoso'